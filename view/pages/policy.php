1.1.3. გამოყენების წესები - ნიშნავს წინამდებარე წესებს, ნებისმიერ სხვა წესთან ან/და პირობასთან ერთად, რომელსაც ეს გამოყენების წესები შესაბამისი მითითებით მიიჩნევენ ამ წესების ნაწილად და რომლებიც არეგულირებენ მომხმარებელისა და მერჩანტის მიერ პლატფორმაზე წვდომისა და მისი გამოყენების საკითხებს, მათ შორის, დაკავშირებულს ნებისმიერ შინაარსთან, ფუნქციასთან და მომსახურებასთან, რომელიც შეთავაზებულია ვებ-გვერდზე;

1.1.4. ვებ-გვერდი - ნიშნავს iland.ge-ს, რომლის მეშვეობითაც ხდება მომსახურების შეთავაზება;

1.1.5. მომხმარებელი (მყიდველი) - იურიდიული ან ფიზიკური პირი, რომელიც აკმაყოფილებს წინამდებარე პირობებით გათვალისწინებულ მოთხოვნებს და სურს iland.ge-ს პლატფორმის საშუალებით შეიძინოს მერჩანტის პროდუქცია;

1.1.6. მერჩანტი - იურიდიული ან ფიზიკური პირი, რომელიც აკმაყოფილებს წინამდებარე პირობებით გათვალისწინებულ მოთხოვნებს და სურს პლატფორმის საშუალებით მოახდინოს პროდუქციის რეალიზაცია;

