წესები და პირობები

შესავალი

წინამდებარე წესები და პირობები წარმოადგენს თქვენსა და შპს - „iland“ (შემდგომში „iland“ ან iland.ge) შორის დადებულ სავალდებულო ძალის მქონე შეთანხმებას და არეგულირებს თქვენ მიერ iland.ge ვებ-გვერდის გამოყენებას. iland.ge-ს ვებ-გვერდი ან/და მასთან დაკავშირებული მომსახურებები, მათ შორის ilandს მიერ მერჩანტებისა და მომხმარებლების მისამართით განხორციელებული ნებისმიერი სახის კომუნიკაცია შემოთავაზებულ მომსახურებებთან დაკავშირებით, შემდგომში ერთობლივად წოდებულია, როგორც iland.ge-ს პლატფორმა.

პერსონალური ინფორმაცია, რომელსაც iland თქვენგან ან მესამე პირებისგან მოიპოვებს მუშავდება წინამდებარე წესებში განსაზღვრული კონფიდენციალურობის პოლიტიკის შესაბამისად.

iland.ge-ს გამოყენების წესებსა და პირობებზე თანხმობით, თქვენ ასევე ეთანხმებით iland.ge-ს კონფიდენციალურობის პოლიტიკას.

iland.ge-ს გამოყენების წესებსა და პირობებზე თანხმობით, თქვენ ასევე ეთანხმებით iland.ge-ს ნივთის დაბრუნების/შეცვლის პოლიტიკას.

ვინაიდან მომხმარებელსა და მერჩანტს აქვთ სურვილი, გახსნან iland.ge-ს მიერ ოპერირებად პლატფორმაზე პირადი ანგარიში რათა, შესაბამისად, მომხმარებელმა პლატფორმის მეშვეობით შეიძინოს, ხოლო მერჩანტმა მოახდინოს საკუთარი პროდუქციის რეალიზაცია, iland, მომხმარებელი და მერჩანტი თანხმდებიან, დადონ ხელშეკრულება, რომლის საფუძველზეც მოხდება მომხმარებელისა და მერჩანტის მიერ ანგარიშის გახსნა, გამოყენება, iland.ge-ს მიერ მის პლატფორმაზე განთავსებული პროდუქციის გაყიდვა და მომხმარებელისთვის მიწოდება, მომხმარებელის მიერ საფასურის გადახდა, მერჩანტის ანაზღაურება, პლატფორმის საშუალებით განხორციელებული გაყიდვებიდან საკომისიოს დაანგარიშება და ანაზღაურების წესი, ასევე ანგარიშის დახურვა.

გთხოვთ, ვებ-გვერდის გამოყენებამდე ყურადღებით გაეცნოთ წინამდებარე გამოყენების წესებს.

ყველა მომხმარებელი და მერჩანტი, რომელსაც სურს iland.ge-ს პლატფორმით სარგებლობა, ვალდებულია დაეთანხმოს და დაიცვას ქვემოთ მოყვანილი წესები და პირობები. iland.ge-ის ადმინისტრაცია უფლებას იტოვებს, ნებისმიერ დროს ცალმხრივად, ამავე ვებ-გვერდზე გამოქვეყნების გზით, შეიტანოს ცვლილებები ამ პირობებში მომხმარებელისა და მერჩანტის შემდგომი დამატებითი თანხმობის გარეშე.

ტერმინთა განმარტება
1.1. წინამდებარე პირობებში გამოყენებულ ტერმინებს და განმარტებებს აქვთ ქვემოთ განსაზღვრული მნიშვნელობა, გარდა იმ შემთხვევებისა, თუ დოკუმენტის კონტექსტიდან აშკარად სხვა რამ არ გამომდინარეობს:

1.1.1. ანგარიში - ნიშნავს მომხმარებელის ან მერჩანტის მიერ ვებ-გვერდზე რეგისტრირებულ ანგარიშს;

