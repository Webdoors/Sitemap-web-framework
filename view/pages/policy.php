1.1.12. უნივერსალური იდენტიფიკატორები - iland.ge-ს მიერ მომხმარებლისათვის მიწოდებული მონაცემების ერთობლიობა, რომელთა გამოყენებითაც იგი რეგისტრირდება იდენტიფიკაციის სისტემაში და შესაძლებლობა ეძლევა ისარგებლოს iland.ge-ს რიგი დისტანციური მომსახურებებით ან/და დისტანციური მომსახურების არხებით;

