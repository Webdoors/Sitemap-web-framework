1.2. სადაც კონტექსტი ამის საშუალებას იძლევა, მხოლობითი რიცხვის აღმნიშვნელი სიტყვები ნიშნავს მრავლობითს და პირიქით.

 

2.განცხადებები და გარანტიები

2.წინამდებარე წესებსა და პირობებზე თანხმობით, მომხმარებელი/მერჩანტი ადასტურებს, რომ:

2.1. მერჩანტი არის დამოუკიდებელი კონტრაქტორი და არ წარმოადგენს ilandს დასაქმებულს, პარტნიორს ან/და აგენტს. iland არ არის პასუხისმგებელი მერჩანტის მიერ პლატფორმაზე სარეალიზაციოდ განთავსებული პროდუქციის ხარისხსა და მისი მახასიათებლების ფაქტობრივ შესაბამისობაზე iland-ს ვებ-გვერდზე (iland.ge) მითითებულ აღწერილობასთან.

2.2. iland თავისუფლდება ყოველგვარი პასუხისმგებლობისგან, რომელიც შეიძლება მოჰყვეს მერჩანტის მიერ განთავსებული პროდუქციის რეალიზაციას, მათ შორის, ხელშეკრულებისა და გარანტიის პირობების დარღვევას, მოქმედ კანონმდებლობასა და რეგულაციებთან შეუსაბამობას და ა.შ.

2.3. არის საქართველოს კანონმდებლობის შესაბამისად დაფუძნებული იურიდიული პირი ან ფიზიკური პირი 18 ან მეტი წლის და ამავდროულად არ არის ევროკავშირის მოქალაქე ან ევროკავშირის წევრი ქვეყნის რეზიდენტი პირი.

2.4. გააჩნია სრული (შეუზღუდავი) ქმედუნარიანობა, არ იმყოფება ნარკოტიკული, ალკოჰოლური, ფსიქოტროპული ან ტოქსიკური პრეპარატების ზემოქმედების ქვეშ, არ იმყოფება შეცდომის, თაღლითობის, ზეწოლის, მუქარისა ან ნებისმიერი მართლსაწინააღმდეგო ზემოქმედების ქვეშ და არ წარმოადგენს ძალადობის, მუქარის, მოტყუების, შეცდომაში შეყვანისა თუ სხვა აკრძალული მოქმედების ობიექტს iland.ge-ს ან ნებისმიერი მესამე პირის მხრიდან, სრულად აცნობიერებს საკუთარი ნების გამოვლენის შინაარსს, პირობების დებულებათა არსს და მისგან გამომდინარე სამართლებრივ შედეგებს;

